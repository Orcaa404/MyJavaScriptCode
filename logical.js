//Logical operators

//1)Logical &&
let a = 4;
let b = 5;
let condition1 = a<b;
let condition2 = a===4;
console.log("condition1 && condition2=",condition1 && condition2);

//2)logical OR ||
let A =9;
let B = 79;
let statement1 = A===B;
let statement2 = A<B;
console.log("statement1 || statement2=",statement1 || statement2);

//logical not !
console.log("!(A>B)=", !(A>B));
